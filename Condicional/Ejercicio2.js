//  •Pide un número.
//  •Si es positivo, muestra "Es positivo".
//  •Si es negativo, muestra "Es negativo".
//  •Si es cero, muestra "Es cero".

let numero = prompt("Por favor, introduce un número:");
if (numero > 0) {
    console.log("Es positivo");
}
else if (numero < 0) {
    console.log("Es negativo");
}
else {
    console.log("Es cero");
}
